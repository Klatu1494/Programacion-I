#include <iostream>

using namespace std;

int main()
{
    int a = 0;
    int b = 0;
    cout << "Ingrese un numero: ";
    cin >> a;
    cout << "Ingrese otro numero: ";
    cin >> b;
    cout << "Su suma es " << a+b << endl;
    cout << "Su diferencia es " << a-b << endl;
    cout << "Su producto es " << a*b << endl;
    return 0;
}
